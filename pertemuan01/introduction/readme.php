// 2 bentuk gaya pemrograman:

// 1. PROCEDURAL Programming
// merupakan sebuah gaya pemrograman yang pernah digunakan di seri phpdasar
// karakteristik :
// setiap instruksi dilakukan langkah demi langkah / dalam artian lain setiap instruksi dikerjakan secara runut dalam satu halaman serta step-by-step nya seperti apa.
// modularitas => sebuah metode / teknik yang bertujuan memecah program menjadi bagian bagian kecil.
// dalam bahasa pemrograman lainnya, istilah gaya ini biasa disebut: prosedur, function, sub-routine.
// disebut juga linear karena program dibaca dari atas - bawah.
// bahasa pemrograman yang mengimplementasikan gaya ini diantaranya :
// FORTRAN, COBOL, ALGOL, PASCAL, C, PHP, JS.
// PHP dan JS merupakan bahasa pemrograman yang bisa diimplementasikan secara procedural maupun ubject oriented.
// KELEBIHAN:
// To-the-point: kita bisa langsung melakukan apa yang kita mau dengan menuliskan baris baris tertentu. Sedangkan Object Oriented cenderung lebih kepada harus melakukan pendefinisian / mendefinisikan sesuatu terlebih dahulu sebelum melakukan sesuatu.
// Simplicity: kemudahan u/ dibaca oleh interpreter maupun compiler.
// Kemudahan penelusuran: dikarenakan gaya penulisannya linear, kesalahan pada suatu baris / program bisa langsung ditelusuri letak kesalahannya.
// Memory Friendly, jika dibanding OOP

// 2. Object Oriented Programming
// menyusun semua code dan struktur data sebagai object.
// object sendiri merupakan unit dasar dari sebuah program.
// object menyimpan data dan perilaku, jadi nantinya kita cenderung tidak memiliki variabel dan function melainkan data dan perilaku yang nantinya disebut dengan istilah property dan method.
// Object bisa saling berinteraksi.
// bahasa pemrograman yang mengadaptasi gaya pemrograman ini diantaranya:
// JAVA, RUBY, Python, C++, JS, PHP 5 keatas.
// KELEBIHAN:
// Representasi dunia nyata: dengan menggunakan paradigma Object Oriented ini kita akan lebih mudah untuk merepresentasikan sesuatu yang ada di dunia nyata yang nantinya akan menjadi kebutuhan dari program
// Enkapsulasi & Abstraksi:
// Enkapsulasi => Memungkinkan data yang kita buat aman ( tidak bisa di akses oleh bagian lain dari program )
// Abstraksi => Mempermudah dalam menyembunyikan kerumitan dalam program
// Reusability: dapat dengan mudah untuk menggunakan ulang bagian - bagian dari program yang sudah dibuat
// Skalabilitas & Ekstansibilitas: memudahkan pengembang nantinya jika programnya sudah besar
